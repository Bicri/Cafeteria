    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
       
        if(txtNombre.getText().isEmpty() || txtPrecio.getText().isEmpty() )
        {
            lblError.setVisible(true);
            lblError.setText("Error de campos vac√≠os");
        }
        else
        {
            lblError.setVisible(false);
               
            plato = new Platillo(lblId2.getText(), txtNombre.getText(), Float.parseFloat(txtPrecio.getText()));
            mysql.conectar();
            mysql.insertaPlatillo(plato);
            mysql.desconectar();
            dispose();
            
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void txtPrecioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrecioKeyTyped
        char caracter = evt.getKeyChar();
        
       if(Character.isLetter(caracter) || caracter == KeyEvent.VK_SPACE  || caracter==',' || (caracter == '.' && txtPrecio.getText().length()==0))
        {
            getToolkit().beep();
            evt.consume();
        }
        else 
       {
            if(txtPrecio.getText().contains(".") && caracter == '.')
            {
                evt.consume();
            }
       }
       
       lblError.setVisible(false);
    }//GEN-LAST:event_txtPrecioKeyTyped

    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
        // TODO add your handling code here:
        char caracter = evt.getKeyChar();
        
       if(txtNombre.getText().length() == 28 || caracter==',')
        {
            getToolkit().beep();
            evt.consume();
        }
       lblError.setVisible(false);
    }//GEN-LAST:event_txtNombreKeyTyped

